	struct Visibility
	{
		// User fills these:
		uint32_t layerMask = ~0u;
		const chif::scene::Scene* scene = nullptr;
		const chif::scene::CameraComponent* camera = nullptr;
		enum FLAGS
		{
			EMPTY = 0,
			ALLOW_OBJECTS = 1 << 0,
			ALLOW_LIGHTS = 1 << 1,
			ALLOW_DECALS = 1 << 2,
			ALLOW_ENVPROBES = 1 << 3,
			ALLOW_EMITTERS = 1 << 4,
			ALLOW_HAIRS = 1 << 5,
			ALLOW_REQUEST_REFLECTION = 1 << 6,
			ALLOW_OCCLUSION_CULLING = 1 << 7,
			ALLOW_SHADOW_ATLAS_PACKING = 1 << 8,

			ALLOW_EVERYTHING = ~0u
		};
		uint32_t flags = EMPTY;

		// chif::renderer::UpdateVisibility() fills these:
		chif::primitive::Frustum frustum;
		chif::vector<uint32_t> visibleObjects;
		chif::vector<uint32_t> visibleDecals;
		chif::vector<uint32_t> visibleEnvProbes;
		chif::vector<uint32_t> visibleEmitters;
		chif::vector<uint32_t> visibleHairs;
		chif::vector<uint32_t> visibleLights;
		chif::rectpacker::State shadow_packer;
		chif::rectpacker::Rect rain_blocker_shadow_rect;
		chif::vector<chif::rectpacker::Rect> visibleLightShadowRects;

		std::atomic<uint32_t> object_counter;
		std::atomic<uint32_t> light_counter;

		chif::SpinLock locker;
		bool planar_reflection_visible = false;
		float closestRefPlane = std::numeric_limits<float>::max();
		XMFLOAT4 reflectionPlane = XMFLOAT4(0, 1, 0, 0);
		std::atomic_bool volumetriclight_request{ false };
		std::atomic_bool transparents_visible{ false };

		void Clear()
		{
			visibleObjects.clear();
			visibleLights.clear();
			visibleDecals.clear();
			visibleEnvProbes.clear();
			visibleEmitters.clear();
			visibleHairs.clear();

			object_counter.store(0);
			light_counter.store(0);

			closestRefPlane = std::numeric_limits<float>::max();
			planar_reflection_visible = false;
			volumetriclight_request.store(false);
			transparents_visible.store(false);
		}

		bool IsRequestedPlanarReflections() const
		{
			return planar_reflection_visible;
		}
		bool IsRequestedVolumetricLights() const
		{
			return volumetriclight_request.load();
		}
		bool IsTransparentsVisible() const
		{
			return transparents_visible.load();
		}
	};

